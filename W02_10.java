package tutorialworkshop_02;

public class W02_10 {
    public static void main(String[] args) {
        int roll_no = 12;    
        switch (roll_no) {
            case 1:
                System.out.println("Your roll number is 10");
                break;
            case 2:
                System.out.println("Your roll number is 12");
                break;
            default:
                System.out.println("Your roll number is greater than 12");
        }
    }
}
